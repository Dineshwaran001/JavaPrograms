package ex_08_For_Loop;

public class Lab059_For_Long_double {
    public static void main(String[] args) {
        for (long i =1l; i<=5; i++){ // you can use any data type for for loop
            System.out.println(i);
        }
        // float double is not advisable
        for(float j =0.0f; j<5.5 ; j++){ //  execute 6 times
            System.out.println(j);
        }

    }
}
