package ex_08_For_Loop;

public class Lab060_Concept_0_And_1 {
    public static void main(String[] args) {
        for (int i=0 ;i<5; i++){// it will execute five times-> 0 to 4
            System.out.println(i);
        }
        //if you want to use 1 instead of 0 use '=' -> in condition
        for (int j=1 ;j<=5; j++){// it will execute five times -> 1 to 5
            System.out.println(j);
        }
    }
}
